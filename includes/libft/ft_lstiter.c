#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	if (lst && f)
	{
		f(lst);
		if (lst->prev)
			ft_lstiter(lst->prev, f);
		if (lst->next)
			ft_lstiter(lst->next, f);
	}
}
